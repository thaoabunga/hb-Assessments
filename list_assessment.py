    """Return a list of only the odd numbers in the input list.

    For example::

        >>> all_odd([1, 2, 7, -5])
        [1, 7, -5]

        >>> all_odd([2, -6, 8])
        []
    """
    oddsList = [x for x in numbers if x % 2 == 1] #for a number in the list of numbers if number is divided by 2 equates to 1, the number is odd
